"""PAF-7
A conveyor belt has packages that must be shipped from one port to another within days.
The ith package on the conveyor belt has a weight of weights[i].
Each day, we load the ship with packages on the conveyor belt (in the order given by weights).
We may not load more weight than the maximum weight capacity of the ship.
Return the least weight capacity of the ship
that will result in all the packages on the conveyor belt being shipped within days.
"""
