def weight_capacity1(weights: List[int], days: int) -> int:
    if days == 1:
        return sum(weights)
    capacity = max(max(weights), sum(weights)//days)
    while True:
        day, daily_load = 0, 0
        for pack in weights:
            if daily_load + pack > capacity:
                day += 1
                daily_load = 0
            daily_load += pack
        day += 1
        if day <= days:
            break
        else:
            capacity += 1
    return capacity


def weight_capacity2(weights: List[int], days: int) -> int:
    def feasible(capacity) -> bool:
        day = 1
        total = 0
        for weight in weights:
            total += weight
            if total > capacity:  # too heavy, wait for the next day
                total = weight
                day += 1
                if day > days:  # cannot ship within 'days' days
                    return False
        return True
    left, right = max(weights), sum(weights)
    while left < right:
        mid = (left + right) // 2
        if feasible(mid):
            right = mid
        else:
            left = mid + 1
    return left


