def weight_capacity(weights: List[int], days: int) -> int:
    """
    Function returns the least weight capacity of the ship,
    that will result in all the packages on the conveyor belt being shipped within days.

    :param weights: List[int]
    :param days: int
    :return capacity: int
    """
    capacity = max(weights)

    while True:
        # day, daily_load, load_ship, ship, conveyor_belt = 0, 0, [], [], deque(weights)
        day, daily_load, load_ship, ship = 0, 0, [], []
        for pack in weights:
            # pack = conveyor_belt.popleft()
            if daily_load + pack > capacity:
                ship.append(load_ship)
                day += 1
                daily_load, load_ship = 0, []
            daily_load += pack
            load_ship.append(pack)
        ship.append(load_ship)
        day += 1
        if day <= days:
            break
        else:
            capacity += 1

    print("The minimum weight capacity of the ship is {0}.".format(str(capacity)))
    for i in range(len(ship)):
        print(f"Day {str(i+1)}: {ship[i]}  ", end='')
    return capacity


